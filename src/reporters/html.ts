/**
 * Self-contained interactive HTML report.
 *
 * Design goals:
 *   - Single file: inline CSS + vanilla JS, no CDN, no external fonts.
 *     The output renders the same on a developer's laptop, in CI artifacts,
 *     and in a thesis-presentation context (offline).
 *   - Mode-aware: review mode shows reviewer health + findings; fix mode
 *     adds a before/after delta, per-iteration timeline, and the resolved
 *     vs. introduced split per iteration.
 *   - Filter + sort client-side: agreement, severity, free-text search, and
 *     the option to peek at baseline-suppressed findings without re-running.
 *   - XSS-safe: every untrusted string is HTML-escaped; the embedded JSON
 *     payload is escaped with `\u003C` so a `</script>` in a title can't
 *     break out of the data block.
 */

interface ReportShell {
  title: string;
  subtitle: string;
  data: unknown;
  body: string;
}

export function renderReviewHtml(output: ReviewModeOutput): string {
  const data = {
    mode: 'review' as const,
    findings: output.findings,
    baselineSuppressed: output.baselineSuppressed ?? [],
    perReviewer: output.perReviewer.map((r) => ({
      reviewer: r.reviewer,
      findings: r.findings.length,
      costUSD: r.usage.costUSD,
      durationMs: r.durationMs,
      status: r.status,
      error: r.error,
    })),
  };

  const body = [
    summaryCards({
      breakdown: output.breakdown,
      totalCostUSD: output.totalCostUSD,
      totalDurationMs: output.totalDurationMs,
      totalFindings: output.findings.length,
      baselineSuppressed: data.baselineSuppressed.length,
      reviewStatus: output.reviewStatus,
      failedReviewers: output.failedReviewers,
    }),
    reviewerHealthSection(data.perReviewer),
    sastSection(output.sast),
    findingsSection(output.findings),
  ].join('\n');

  return shell({
    title: 'Secure Review Report',
    subtitle: `Generated ${new Date().toISOString()}`,
    data,
    body,
  });
}

export function renderFixHtml(output: FixModeOutput): string {
  const data = {
    mode: 'fix' as const,
    findings: output.finalFindings,
    baselineSuppressed: output.baselineSuppressed ?? [],
    iterations: output.iterations.map((it) => ({
      iteration: it.iteration,
      reviewer: it.reviewer,
      findingsBefore: it.findingsBefore.length,
      findingsAfter: it.findingsAfter.length,
      resolved: it.resolvedFindings,
      introduced: it.introducedFindings,
      newCritical: it.newCritical,
      costUSD: it.costUSD,
    })),
  };

  const body = [
    summaryCards({
      breakdown: output.finalBreakdown,
      totalCostUSD: output.totalCostUSD,
      totalDurationMs: output.totalDurationMs,
      totalFindings: output.finalFindings.length,
      baselineSuppressed: data.baselineSuppressed.length,
      reviewStatus: output.reviewStatus,
      failedReviewers: output.failedReviewers,
      gateBlocked: output.gateBlocked,
      gateReasons: output.gateReasons,
    }),
    beforeAfterSection(output.initialBreakdown, output.finalBreakdown, output.initialFindings.length, output.finalFindings.length),
    iterationsSection(output.iterations),
    filesChangedSection(output.filesChanged),
    findingsSection(output.finalFindings, 'Remaining findings'),
  ].join('\n');

  return shell({
    title: 'Secure Review — Fix Mode',
    subtitle: `Generated ${new Date().toISOString()}`,
    data,
    body,
  });
}

