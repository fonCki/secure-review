interface SummaryCardsInput {
  breakdown: SeverityBreakdown;
  totalCostUSD: number;
  totalDurationMs: number;
  totalFindings: number;
  baselineSuppressed: number;
  reviewStatus: 'ok' | 'degraded' | 'failed';
  failedReviewers: string[];
  gateBlocked?: boolean;
  gateReasons?: string[];
}

function summaryCards(s: SummaryCardsInput): string {
  const statusLabel =
    s.reviewStatus === 'ok'
      ? `<span class="status ok">OK</span>`
      : s.reviewStatus === 'degraded'
        ? `<span class="status degraded">DEGRADED</span><span class="status-detail">${escapeHtml(s.failedReviewers.join(', '))}</span>`
        : `<span class="status failed">FAILED</span>`;
  const gate =
    s.gateBlocked === undefined
      ? ''
      : s.gateBlocked
        ? `<div class="card stat warn">
            <div class="stat-label">Gate</div>
            <div class="stat-value">BLOCKED</div>
            <div class="stat-foot">${escapeHtml((s.gateReasons ?? []).join(' · ') || '—')}</div>
          </div>`
        : `<div class="card stat ok">
            <div class="stat-label">Gate</div>
            <div class="stat-value">PASSED</div>
          </div>`;
  return `<section class="grid">
  <div class="card stat">
    <div class="stat-label">Findings</div>
    <div class="stat-value">${s.totalFindings}</div>
    ${s.baselineSuppressed > 0 ? `<div class="stat-foot muted">+${s.baselineSuppressed} baselined</div>` : ''}
  </div>
  <div class="card stat">
    <div class="stat-label">Cost</div>
    <div class="stat-value">$${s.totalCostUSD.toFixed(3)}</div>
    <div class="stat-foot muted">${(s.totalDurationMs / 1000).toFixed(1)}s</div>
  </div>
  <div class="card stat">
    <div class="stat-label">Status</div>
    <div class="stat-value">${statusLabel}</div>
  </div>
  ${gate}
  <div class="card breakdown">
    <div class="stat-label">Severity</div>
    <div class="bars">
      ${severityBar('CRITICAL', s.breakdown.CRITICAL)}
      ${severityBar('HIGH', s.breakdown.HIGH)}
      ${severityBar('MEDIUM', s.breakdown.MEDIUM)}
      ${severityBar('LOW', s.breakdown.LOW)}
      ${severityBar('INFO', s.breakdown.INFO)}
    </div>
  </div>
</section>`;
}

function severityBar(severity: string, count: number): string {
  return `<div class="bar bar-${severity.toLowerCase()}">
    <span class="bar-label">${severity}</span>
    <span class="bar-count">${count}</span>
  </div>`;
}

function reviewerHealthSection(
  perReviewer: { reviewer: string; findings: number; costUSD: number; durationMs: number; status: string; error?: string }[],
): string {
  if (perReviewer.length === 0) return '';
  const rows = perReviewer
    .map(
      (r) => `<tr>
        <td>${escapeHtml(r.reviewer)}</td>
        <td class="num">${r.findings}</td>
        <td class="num">$${r.costUSD.toFixed(3)}</td>
        <td class="num">${(r.durationMs / 1000).toFixed(1)}s</td>
        <td>${r.error ? `<span class="status failed">FAILED</span><span class="status-detail">${escapeHtml(r.error)}</span>` : `<span class="status ok">${escapeHtml(r.status)}</span>`}</td>
      </tr>`,
    )
    .join('\n');
  return `<section class="card pad">
    <h2>Per-reviewer health</h2>
    <table><thead><tr>
      <th>Reviewer</th><th>Findings</th><th>Cost</th><th>Duration</th><th>Status</th>
    </tr></thead><tbody>${rows}</tbody></table>
  </section>`;
}

function sastSection(sast: ReviewModeOutput['sast']): string {
  const row = (name: string, r: { ran: boolean; count: number; error?: string }) =>
    `<tr>
      <td>${escapeHtml(name)}</td>
      <td>${r.ran ? '✓' : '⚠︎'}</td>
      <td class="num">${r.count}</td>
      <td class="muted">${escapeHtml(r.error ?? '')}</td>
    </tr>`;
  return `<section class="card pad">
    <h2>SAST</h2>
    <table><thead><tr><th>Tool</th><th>Ran?</th><th>Findings</th><th>Note</th></tr></thead>
    <tbody>
      ${row('semgrep', sast.semgrep)}
      ${row('eslint', sast.eslint)}
      ${row('npm-audit', sast.npmAudit)}
    </tbody></table>
  </section>`;
}

function beforeAfterSection(
  initial: SeverityBreakdown,
  final: SeverityBreakdown,
  initialTotal: number,
  finalTotal: number,
): string {
  const sevs: (keyof SeverityBreakdown)[] = ['CRITICAL', 'HIGH', 'MEDIUM', 'LOW', 'INFO'];
  const rows = sevs
    .map((k) => {
      const d = final[k] - initial[k];
      const cls = d < 0 ? 'good' : d > 0 ? 'bad' : 'flat';
      const sign = d > 0 ? '+' : '';
      return `<tr>
        <td><span class="sev-pill bar-${k.toLowerCase()}">${k}</span></td>
        <td class="num">${initial[k]}</td>
        <td class="num">${final[k]}</td>
        <td class="num delta ${cls}">${sign}${d}</td>
      </tr>`;
    })
    .join('\n');
  const totalDelta = finalTotal - initialTotal;
  const totalCls = totalDelta < 0 ? 'good' : totalDelta > 0 ? 'bad' : 'flat';
  return `<section class="card pad">
    <h2>Before vs after</h2>
    <table>
      <thead><tr><th>Severity</th><th>Initial</th><th>Final</th><th>Δ</th></tr></thead>
      <tbody>${rows}</tbody>
      <tfoot><tr>
        <td><strong>Total</strong></td>
        <td class="num"><strong>${initialTotal}</strong></td>
        <td class="num"><strong>${finalTotal}</strong></td>
        <td class="num delta ${totalCls}"><strong>${totalDelta > 0 ? '+' : ''}${totalDelta}</strong></td>
      </tr></tfoot>
    </table>
  </section>`;
}

function iterationsSection(iterations: IterationRecord[]): string {
  if (iterations.length === 0) return '';
  const rows = iterations
    .map(
      (it) => `<tr>
        <td class="num">${it.iteration}</td>
        <td>${escapeHtml(it.reviewer)}</td>
        <td class="num">${it.findingsBefore.length}</td>
        <td class="num">${it.findingsAfter.length}</td>
        <td class="num good">${it.resolved}</td>
        <td class="num ${it.introducedFindings.length > 0 ? 'bad' : 'flat'}">${it.introducedFindings.length}${it.newCritical > 0 ? ` <span class="crit-tag">(${it.newCritical} CRITICAL)</span>` : ''}</td>
        <td class="num">$${it.costUSD.toFixed(3)}</td>
      </tr>`,
    )
    .join('\n');

  const detail = iterations
    .map((it) => {
      if (it.resolvedFindings.length === 0 && it.introducedFindings.length === 0) return '';
      return `<details class="card iter-detail">
          <summary>Iteration ${it.iteration} — ${escapeHtml(it.reviewer)}: <span class="good">${it.resolvedFindings.length} resolved</span> · <span class="bad">${it.introducedFindings.length} introduced</span></summary>
          ${deltaList('Resolved', it.resolvedFindings, 'good')}
          ${deltaList('Introduced', it.introducedFindings, 'bad')}
        </details>`;
    })
    .join('\n');

  return `<section class="card pad">
    <h2>Iterations</h2>
    <table>
      <thead><tr><th>#</th><th>Verifier</th><th>In</th><th>Out</th><th>Resolved</th><th>Introduced</th><th>Cost</th></tr></thead>
      <tbody>${rows}</tbody>
    </table>
    <p class="muted small">
      <em>In</em> = what the writer addressed this iteration (initial union for iter 1, prior verifier audit for iter 2+). <em>Out</em> = what the rotating verifier saw afterwards. Same bug → same <code>S-NNN</code> across iterations (see <code>findings/identity.ts</code>).
    </p>
    ${detail ? `<div class="iter-details">${detail}</div>` : ''}
  </section>`;
}

function deltaList(label: string, findings: Finding[], cls: string): string {
  if (findings.length === 0) return '';
  const items = findings
    .map((f) => {
      const id = f.stableId ? `<code class="stable-id">${escapeHtml(f.stableId)}</code> ` : '';
      const cwe = f.cwe ? `<span class="tag">${escapeHtml(f.cwe)}</span>` : '';
      return `<li>${id}<span class="sev-pill bar-${f.severity.toLowerCase()}">${escapeHtml(f.severity)}</span> <code>${escapeHtml(f.file)}:${f.lineStart}</code> — ${escapeHtml(f.title)} ${cwe}</li>`;
    })
    .join('\n');
  return `<div class="delta-block ${cls}"><h4>${label} (${findings.length})</h4><ul>${items}</ul></div>`;
}

function filesChangedSection(files: string[]): string {
  if (files.length === 0) return '';
  return `<section class="card pad">
    <h2>Files changed (${files.length})</h2>
    <ul class="filelist">${files.map((f) => `<li><code>${escapeHtml(f)}</code></li>`).join('')}</ul>
  </section>`;
}

