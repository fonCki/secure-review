function escapeHtml(s: unknown): string {
  return String(s ?? '').replace(/[&<>"']/g, (c) => {
    switch (c) {
      case '&':
        return '&amp;';
      case '<':
        return '&lt;';
      case '>':
        return '&gt;';
      case '"':
        return '&quot;';
      default:
        return '&#39;';
    }
  });
}

/**
 * Stringify `data` for safe embedding inside `<script type="application/json">`.
 * Replaces `<` with `\u003C` so a `</script>` substring inside a finding title
 * cannot terminate the data block (the spec requires a literal `</script` in
 * raw text — escaping `<` covers it without escaping `/`).
 *
 * Also escapes U+2028 and U+2029 so the embedded JSON parses identically when
 * the surrounding `<script>` happens to be served as JS (e.g. a future move
 * to `type="module"` or a tooling regression that strips the JSON type).
 */
function escapeJsonForScript(data: unknown): string {
  return JSON.stringify(data)
    .replace(/</g, '\\u003C')
    .replace(/\u2028/g, '\\u2028')
    .replace(/\u2029/g, '\\u2029');
}
